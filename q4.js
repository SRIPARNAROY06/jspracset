// setTimeout()method executes the function only once after waiting for a specific time interval.
// Syntax:
// setTimeout(function, delay);
// where function = this function is executed only once after delay milliseconds.
// delay=The time, in milliseconds that the timer should wait before the specified function is executed.
//Example:
// setTimeout(()=>{
//     console.log("SRIPARNA");
// },5000);



// setInterval()method executes the function repeatedly  with a fixed time delay between each call.
// Syntax:
// setInterval(function,delay);
// where function =The first execution of this function happens after delay milliseconds and is executed repeatedly thereafter.
// delay=The time, in milliseconds,the timer should delay in between executions of the specified function.
// Example:
// let i=0;
// setInterval(()=>{
//     console.log("SRIPARNA",i);
//     i++;
// },3000);